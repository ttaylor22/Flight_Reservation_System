package com.group.model;

public class Passenger_Detail {
	private int passenger_detial_id;
	private String full_name;
	private int age;
	private String gender;
	private int seat_no;
	public int getPassenger_detial_id() {
		return passenger_detial_id;
	}
	public void setPassenger_detial_id(int passenger_detial_id) {
		this.passenger_detial_id = passenger_detial_id;
	}
	public String getFull_name() {
		return full_name;
	}
	public void setFull_name(String full_name) {
		this.full_name = full_name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getSeat_no() {
		return seat_no;
	}
	public void setSeat_no(int seat_no) {
		this.seat_no = seat_no;
	}
	
}
